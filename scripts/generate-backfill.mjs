/**
 * Generates a SQL backfill script for existing staging/prod DBs after a journal rebuild.
 *
 * PREFERRED PATH: For brownfield DBs using scripts/migrate.ts (pnpm db:migrate),
 * simply run `pnpm db:migrate` — it handles idempotent application of the 40 new
 * journal entries automatically (ER_DUP_FIELDNAME and CREATE TABLE IF NOT EXISTS
 * are no-ops; the hash is recorded either way). Use this backfill script only when
 * pnpm db:migrate fails unexpectedly or for emergency manual intervention.
 *
 * IMPORTANT: This script targets the __drizzle_migrations schema created by
 * scripts/migrate.ts: (id BIGINT, hash TEXT, created_at BIGINT). It does NOT
 * have a `tag` column. The hash algorithm matches the custom runner exactly:
 * SHA-256 of the SQL content after stripping --statement-breakpoint markers.
 *
 * Usage:
 *   node scripts/generate-backfill.mjs > scripts/backfill-migrations.sql
 *
 * Then review and run it against staging inside a transaction before prod.
 *
 * Prerequisites:
 *   - scripts/old-journal-tags.txt exists (captured before the journal rebuild)
 *   - drizzle/meta/_journal.json has already been rebuilt (run rebuild-journal.mjs first)
 */

import fs from "fs";
import crypto from "crypto";

const OLD_TAGS_PATH = "scripts/old-journal-tags.txt";
const JOURNAL_PATH = "drizzle/meta/_journal.json";

if (!fs.existsSync(OLD_TAGS_PATH)) {
  console.error(`✗ ${OLD_TAGS_PATH} not found — was it captured before the rebuild?`);
  process.exit(1);
}

if (!fs.existsSync(JOURNAL_PATH)) {
  console.error(`✗ ${JOURNAL_PATH} not found — run rebuild-journal.mjs first`);
  process.exit(1);
}

const newJournal = JSON.parse(fs.readFileSync(JOURNAL_PATH, "utf8"));
const oldTags = new Set(
  fs
    .readFileSync(OLD_TAGS_PATH, "utf8")
    .split("\n")
    .map((l) => l.trim().replace(/^"|"$/g, ""))
    .filter(Boolean),
);

const toBackfill = newJournal.entries.filter((e) => !oldTags.has(e.tag));

if (toBackfill.length === 0) {
  console.error("No new migrations to backfill — journal may not have been rebuilt yet.");
  process.exit(1);
}

/**
 * Mirrors migrationHash() in scripts/migrate.ts exactly.
 * Strips --> statement-breakpoint markers before hashing so that
 * adding/removing breakpoints does not change the hash.
 */
function migrationHash(sqlContent) {
  const segments = sqlContent.split("--> statement-breakpoint");
  return crypto.createHash("sha256").update(segments.join("")).digest("hex");
}

const rows = toBackfill.map((e) => {
  const sqlPath = `drizzle/${e.tag}.sql`;
  if (!fs.existsSync(sqlPath)) {
    console.error(`✗ SQL file not found: ${sqlPath}`);
    process.exit(1);
  }
  const sql = fs.readFileSync(sqlPath, "utf8");
  const hash = migrationHash(sql);
  return { tag: e.tag, hash, when: e.when };
});

const expectedTotal = oldTags.size + rows.length;

// Sample 3 hashes for the spot-check query.
const sampleHashes = rows.slice(0, 3).map((r) => JSON.stringify(r.hash)).join(", ");

const lines = [
  "-- =============================================================================",
  "-- Journal backfill: mark 40 previously-applied migrations as known to Drizzle.",
  "-- Generated by: node scripts/generate-backfill.mjs",
  `-- Generated at: ${new Date().toISOString()}`,
  "--",
  "-- Table: __drizzle_migrations (schema from scripts/migrate.ts — no tag column)",
  "-- Hash algorithm: SHA-256(segments.join('')) — same as scripts/migrate.ts",
  "--",
  "-- PREFERRED: run `pnpm db:migrate` instead. This script is a fallback for",
  "-- cases where db:migrate fails unexpectedly on the 40 new journal entries.",
  "-- Run against staging first. Verify the SELECT results before committing.",
  "-- INSERT IGNORE makes it safe to re-run.",
  "-- =============================================================================",
  "",
  "START TRANSACTION;",
  "",
  `-- Inserting ${rows.length} backfill rows (previously missing from journal):`,
  `-- created_at is stored as milliseconds epoch (matches scripts/migrate.ts behavior).`,
  "INSERT IGNORE INTO __drizzle_migrations (hash, created_at)",
  "VALUES",
  rows.map((r, i) => {
    const comma = i < rows.length - 1 ? "," : ";";
    return `  (${JSON.stringify(r.hash)}, ${r.when})${comma}  -- ${r.tag}`;
  }).join("\n"),
  "",
  "-- ── Verify BEFORE committing ────────────────────────────────────────────────",
  "",
  "-- 1. Total count must match expected:",
  "SELECT COUNT(*) AS total_migrations FROM __drizzle_migrations;",
  `--    Expected: ${expectedTotal} (was ${oldTags.size}, adding ${rows.length})`,
  "",
  "-- 2. Spot-check: confirm at least 3 of the new hashes are present:",
  `SELECT COUNT(*) AS spot_check FROM __drizzle_migrations`,
  `  WHERE hash IN (${sampleHashes});`,
  `--    Expected: 3 (if 0, INSERT IGNORE skipped all rows — hashes may already exist or mismatch)`,
  "",
  "-- 3. Confirm no unexpected extra rows (guards against double-backfill):",
  "SELECT COUNT(*) AS total_migrations FROM __drizzle_migrations;",
  `--    Still expected: ${expectedTotal}`,
  "",
  "-- 4. Verify newly-inserted rows by epoch range",
  `--    (created_at values for new entries: ${rows[0]?.when} – ${rows[rows.length - 1]?.when})`,
  `SELECT COUNT(*) AS in_range FROM __drizzle_migrations`,
  `  WHERE created_at BETWEEN ${rows[0]?.when} AND ${rows[rows.length - 1]?.when};`,
  `--    Expected: ${rows.length} (or more if prior migrations had similar timestamps)`,
  "",
  "-- ── Commit or roll back ──────────────────────────────────────────────────────",
  "",
  "-- If all checks above look correct:",
  "-- COMMIT;",
  "",
  "-- If anything looks wrong:",
  "-- ROLLBACK;",
  "",
  "-- IMPORTANT: uncomment exactly one of COMMIT or ROLLBACK before executing.",
  "-- Leaving both commented means the transaction stays open until the connection closes.",
];

console.log(lines.join("\n"));

process.stderr.write(`\n✓ Generated backfill for ${rows.length} migrations\n`);
process.stderr.write(`  Old journal size : ${oldTags.size}\n`);
process.stderr.write(`  New journal size : ${newJournal.entries.length}\n`);
process.stderr.write(`  Expected DB total: ${expectedTotal}\n`);
process.stderr.write(`  Hash algorithm   : SHA-256(breakpoint-stripped content) — matches scripts/migrate.ts\n`);
process.stderr.write(`  Output           : scripts/backfill-migrations.sql\n\n`);
